function extractWarranty(html, offer) {
  // Depuis l'offre JSON-LD
  if (offer.warranty) return normalizeWarrantyText(String(offer.warranty));

  // Depuis le HTML : cherche "X mois de garantie" ou "garantie X mois"
  const m = html.match(/(\d+)\s*mois?\s*(?:de\s*)?garantie|garantie\s*(\d+)\s*mois/i);
  if (m) return normalizeWarrantyText(`${m[1] || m[2]} mois`);

  // "X an(s) de garantie"
  const m2 = html.match(/(\d+)\s*an(?:s)?\s*(?:de\s*)?garantie|garantie\s*(\d+)\s*an/i);
  if (m2) {
    const months = parseInt(m2[1] || m2[2]) * 12;
    return `${months} mois`;
  }
  return '';
}

function normalizeWarrantyText(raw) {
  const s = (raw || '').trim().toLowerCase();
  const m = s.match(/(\d+)\s*mois/);
  if (m) return `${m[1]} mois`;
  const m2 = s.match(/(\d+)\s*an/);
  if (m2) return `${parseInt(m2[1]) * 12} mois`;
  return raw.trim().slice(0, 30) || '';
}

